package Module1.Chapter1_Introduction.Operator.BinaryOperator;

public class BitwiseOperator {
    public static void main(String[] args) {
        boolean logic1 = true & false;
        System.out.println(logic1);

        boolean logic2 = true | false;
        System.out.println(logic2);

        boolean logic6 = true ^ true;
        System.out.println(logic6);

        int logic3 = 7 | 10;
        System.out.println(logic3);

        int logic4 = 4 & 10;
        System.out.println(logic4);

        int logic5 = 3 ^ 10;
        System.out.println(logic5);
    }
}
