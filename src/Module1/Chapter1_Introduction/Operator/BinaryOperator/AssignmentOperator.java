package Module1.Chapter1_Introduction.Operator.BinaryOperator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        a += 2;
        System.out.println(a);

        a = 10;
        a -= 2;
        System.out.println(a);

        a = 10;
        a *= 2;
        System.out.println(a);

        a = 10;
        a /= 2;
        System.out.println(a);

        a = 10;
        a %= 2;
        System.out.println(a);

        a=10;
        a <<= 2;
        System.out.println(a);

        a = 10;
        a >>= 2;
        System.out.println(a);

        a = 10;
        a &= 2;
        System.out.println(a);

        a = 10;
        a |= 2;
        System.out.println(a);
    }
}
