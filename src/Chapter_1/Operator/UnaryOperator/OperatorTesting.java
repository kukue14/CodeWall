package Chapter_1.Operator.UnaryOperator;

public class OperatorTesting {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);
//        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);

        int b = 5;
        System.out.println(b);
//        System.out.println(--b);
        System.out.println(b--);
        System.out.println(b);
    }
}
